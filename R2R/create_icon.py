#!/usr/bin/env python3
"""
Script simple para crear un icono PNG básico para la extensión R2R
"""

# Crear un icono PNG simple usando bytes directos
png_data = bytes([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,  # PNG signature
    0x00, 0x00, 0x00, 0x0D,  # IHDR chunk length
    0x49, 0x48, 0x44, 0x52,  # IHDR
    0x00, 0x00, 0x00, 0x20,  # width: 32
    0x00, 0x00, 0x00, 0x20,  # height: 32
    0x08, 0x02, 0x00, 0x00, 0x00,  # bit depth, color type, compression, filter, interlace
    0xFC, 0x18, 0xED, 0xA3,  # CRC
    0x00, 0x00, 0x00, 0x09,  # PLTE chunk length
    0x50, 0x4C, 0x54, 0x45,  # PLTE
    0x34, 0x98, 0xDB, 0xFF, 0xFF, 0xFF, 0x00, 0x00, 0x00,  # palette: blue, white, black
    0x5C, 0x92, 0x5A, 0x29,  # CRC
    0x00, 0x00, 0x00, 0x22,  # IDAT chunk length
    0x49, 0x44, 0x41, 0x54,  # IDAT
    0x78, 0x9C, 0x63, 0x60, 0x18, 0x05, 0xA3, 0x60, 0x14, 0x8C, 0x02, 0x08,
    0x00, 0x00, 0x04, 0x10, 0x00, 0x01, 0x2A, 0x0A, 0x2A, 0x0A, 0x2A, 0x0A,
    0x2A, 0x0A, 0x2A, 0x0A, 0x2A, 0x0A, 0x2A, 0x0A, 0x2A, 0x0A,
    0x7E, 0x86, 0x01, 0x24,  # CRC
    0x00, 0x00, 0x00, 0x00,  # IEND chunk length
    0x49, 0x45, 0x4E, 0x44,  # IEND
    0xAE, 0x42, 0x60, 0x82   # CRC
])

# Escribir el archivo
with open('vscode-r2r-activity/icon.png', 'wb') as f:
    f.write(png_data)

print("✅ Icono PNG creado: vscode-r2r-activity/icon.png")
